#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	signo;
	int	num;

	i = 0;
	signo = 1;
	num = 0;
	while ((str[i] >= '\t' && str[i] <= '\r') || str[i] == 32)
	{
		i++;
	}
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			signo = signo *(-1);
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		num = (num * 10) + (str[i] - 48);
		i++;
	}
	return (num * signo);
}
